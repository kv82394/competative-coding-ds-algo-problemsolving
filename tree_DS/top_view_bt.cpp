class Solution
{
    public:
    //Function to return a list of nodes visible from the top view 
    //from left to right in Binary Tree.
    vector<int> topView(Node *root)
    {
        vector<int> ans;
        if (root==NULL)return ans;
        map<int,int> mp;
        queue<pair<int,Node*>> st;
        pair<int,Node*> temp_pair;
        temp_pair.first = 0;
        temp_pair.second = root;
        st.push(temp_pair);
        while(!st.empty()){
            temp_pair = st.front();
            st.pop();
            int nodeindex = temp_pair.first;
            Node *node = temp_pair.second;
            if(mp[nodeindex]==0){
                mp[nodeindex] = node->data;
            }
             if (node->right!=NULL){
             temp_pair.first = nodeindex+1;
             temp_pair.second = node->right;
             //cout<<"xyz  "<<temp_pair.first<<"  "<<temp_pair.second->data<<endl;
             st.push(temp_pair);
            }
            if (node->left!=NULL){
             temp_pair.first = nodeindex-1;
             temp_pair.second = node->left;
             st.push(temp_pair);
            }
             

            
        }
        for (auto it = mp.begin();it!=mp.end();it++){
            ans.push_back(it->second);
        }
        return ans;
        
        
    }

};



